// render products
function renderProducts(category) {
    const container = document.getElementById(category);
    if (!container) return;

    let html = "";

    data[category].forEach((p, index) => {
        html += `
        <div class="product-card">
            <a href="product-detail.html?category=${category}&id=${index}">
                <img src="${p.image}">
            </a>
            <h3>${p.name}</h3>
            <p>Giá: ${p.price}</p>
        </div>
        `;
    });

    container.innerHTML = html;
}

// Hiding + remove class
function hideAll() {
    document.querySelectorAll('.product-section')
        .forEach(sec => {
            sec.style.display = 'none';
            sec.classList.remove('active');
        });
}

// show category
function showCategory(category) {
    hideAll();

    const section = document.getElementById(category);
    if (!section) return;

    // thêm class để CSS xử lý grid
    section.classList.add('active');

    // fallback (phòng trường hợp CSS chưa ăn)
    section.style.display = 'grid';
    section.style.gridTemplateColumns = "repeat(4, 1fr)";
    section.style.gap = "16px";

    // chỉ render khi chưa có dữ liệu
    if (section.innerHTML.trim() === "") {
        renderProducts(category);
    }

    // update URL (xịn hơn)
    window.location.hash = category;
}

// load mặc định + hỗ trợ reload vẫn giữ tab
window.onload = () => {
    const hash = window.location.hash.replace("#", "");
    if (hash && data[hash]) {
        showCategory(hash);
    } else {
        showCategory("keyboard");
    }
};